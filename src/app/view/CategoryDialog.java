    private void fillTable(List<Category> list) {
        int stt = 0;
        tableModelCategory.setRowCount(0);
        for (Category conmpany : list) {
            stt++;
            Object[] row = {
                stt,
                conmpany.getName(),
                conmpany.getDeleted() ? "Ngừng Hoạt Động" : "Hoạt Động"
            };
            tableModelCategory.addRow(row);
        }
    }

    private void addColumnName() {
        tableModelCategory.addColumn("STT");
        tableModelCategory.addColumn("Tên Danh Mục");
        tableModelCategory.addColumn("Trạng Thái");
    }

