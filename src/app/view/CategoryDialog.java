    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if (txtNameCategory.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Tên Màu Không Được Để Trống");
        } else {
            String nameColor = txtNameCategory.getText();
            Category category = new Category();
            category.setName(nameColor);
            category.setDeleted(Boolean.FALSE);
            String result = categoryService.addCategory(category);
            JOptionPane.showMessageDialog(this, result);
            fillTable(categoryService.getAll());
        }
    }//GEN-LAST:event_btnAddActionPerformed

